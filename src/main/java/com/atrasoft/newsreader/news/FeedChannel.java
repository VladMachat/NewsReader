package com.atrasoft.newsreader.news;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import org.hibernate.validator.constraints.NotBlank;


/**
 *
 * @author Atrament
 */
@Entity
public class FeedChannel implements Serializable {

    @Id
    @Column(name = "id")
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int id;

    @NotBlank(message = "Give the channel a name...")
    private String name;

    @NotBlank(message = "Enter the channel url...")
    @org.hibernate.validator.constraints.URL
    private String url;

    private boolean active;

    public FeedChannel() {
        active = true;
    }

    public FeedChannel(String name, String url) {
        this.name = name;
        this.url = url;
        this.active = true;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
